let parseRow (string: string): int =
    string
    |> Seq.fold (fun (lo, hi) substr ->
        match substr with
        | 'F' -> (lo, (lo + hi) / 2)
        | 'B' -> ((lo + hi + 1) / 2, hi)
        | _ -> failwithf "weird seat (row) %A %A" string substr) (0, 127)
    |> (fun (a, _) -> a)

let parseCol (string: string): int =
    string
    |> Seq.fold (fun (lo, hi) substr ->
        match substr with
        | 'L' -> (lo, (lo + hi) / 2)
        | 'R' -> ((lo + hi + 1) / 2, hi)
        | _ -> failwithf "weird seat (col) %A %A" string substr) (0, 7)
    |> (fun (a, _) -> a)

let seatId row col = row * 8 + col

let parseSeat (seat: string): int =
    let rowChars = seat.[..6]
    let colChars = seat.[7..]
    let row = parseRow rowChars
    let col = parseCol colChars
    seatId row col

let part1 (input: string): int =
    input.Split "\n" |> Seq.map parseSeat |> Seq.max

let part2 (input: string): int =
    let seq = input.Split "\n" |> Seq.map parseSeat

    let set: Set<int> = Set.ofSeq seq

    [ 0 .. 806 ]
    |> Seq.find (fun id ->
        Set.contains (id - 1) set
        && Set.contains (id + 1) set
        && not (Set.contains id set))


let main = part2 input
