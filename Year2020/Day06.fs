let computeAnyForGroup (input: string): int =
    input
    |> String.filter System.Char.IsLetter
    |> Set.ofSeq
    |> Set.count

let computeAllForGroup (input: string): int =
    let lines = input.Split "\n"

    let all =
        input
        |> String.filter System.Char.IsLetter
        |> Set.ofSeq

    all
    |> Set.filter (fun char ->
        lines
        |> Seq.forall (fun line -> line.Contains char))
    |> Set.count

let part1 (input: string): int =
    input.Split "\n\n"
    |> Seq.map computeAnyForGroup
    |> Seq.sum

let part2 (input: string): int =
    input.Split "\n\n"
    |> Seq.map computeAllForGroup
    |> Seq.sum

