type Map =
    { Seq: seq<string>
      Width: int
      Height: int
      Right: int
      Down: int }

let toMap (input: string) (right: int, down: int): Map =
    let seq = input.Split '\n'
    { Seq = seq
      Width = seq.[0].Length
      Height = seq.Length
      Right = right
      Down = down }

let rec go1 (treesHit: int) (x: int, y: int) (map: Map): int =
    if y >= map.Height then
        treesHit
    else
        let charAtPosition: char =
            map.Seq |> Seq.item y |> (fun str -> str.[x])

        let (newX, newY) =
            ((x + map.Right) % map.Width, y + map.Down)

        let didHit: bool = charAtPosition = '#'

        let newTreesHit =
            if didHit then treesHit + 1 else treesHit

        go1 newTreesHit (newX, newY) map

let part1 input = go1 0 (0, 0) (toMap input (3, 1))

let allSlopes: (int * int) list =
    [ (1, 1)
      (3, 1)
      (5, 1)
      (7, 1)
      (1, 2) ]

let part2 input =
    allSlopes
    |> List.map (fun slope -> go1 0 (0, 0) (toMap input slope))
    |> List.fold (fun x y -> x * uint64 y) 1UL

let main = part2 realInput
